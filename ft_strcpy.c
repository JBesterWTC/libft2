#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	int		len;
	char	*s1;

	i = 0;
	len = ft_strlen(dst);
	s1 = dst;
	while (i < len)
	{
		*s1++ = *src++;
		i++;
	}
	*s1 = '\0';
	return (dst);
}
